package com.cn.washoes.model;

public class ComInfo {
	private String createtime;
	private String comment_id;
	private String pun_stars;
	private String comments;
	private String att_stars;
	private String pro_stars;

	public String getCreatetime() {
		return createtime;
	}

	public void setCreatetime(String createtime) {
		this.createtime = createtime;
	}

	public String getComment_id() {
		return comment_id;
	}

	public void setComment_id(String comment_id) {
		this.comment_id = comment_id;
	}

	public String getPun_stars() {
		return pun_stars;
	}

	public void setPun_stars(String pun_stars) {
		this.pun_stars = pun_stars;
	}

	public String getComments() {
		return comments;
	}

	public void setComments(String comments) {
		this.comments = comments;
	}

	public String getAtt_stars() {
		return att_stars;
	}

	public void setAtt_stars(String att_stars) {
		this.att_stars = att_stars;
	}

	public String getPro_stars() {
		return pro_stars;
	}

	public void setPro_stars(String pro_stars) {
		this.pro_stars = pro_stars;
	}

}
